package com.alexsandro.cursojava.aula46.labs44a46;

public abstract class FiguraGeometrica {
	public String nome;
	public String cor;

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getCor() {
		return cor;
	}

	public void setCor(String cor) {
		this.cor = cor;
	}

}
